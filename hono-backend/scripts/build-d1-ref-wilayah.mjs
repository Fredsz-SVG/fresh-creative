/**
 * Menghasilkan d1/migrations/0003_seed_ref_indonesia.sql dari Postgres legacy.
 * Jalankan: node scripts/build-d1-ref-wilayah.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '../..')
const legacyPath = path.join(root, 'scripts/ref-indonesia-wilayah.postgres.sql')
const outPath = path.join(__dirname, '../d1/migrations/0003_seed_ref_indonesia.sql')

const legacy = fs.readFileSync(legacyPath, 'utf8')

function esc(s) {
  return s.replace(/'/g, "''")
}

const provSection = legacy.split('INSERT INTO public.ref_provinces')[1].split('ON CONFLICT')[0]
const provinces = [...provSection.matchAll(/\('(\d{2})',\s*'([^']*)'\)/g)]

const cityRe = /\('(\d{4})',\s*'(\d{2})',\s*'([^']*)',\s*'(kabupaten|kota)'\)/g
const cities = [...legacy.matchAll(cityRe)].map((m) => ({
  id: m[1],
  province_id: m[2],
  name: m[3],
  kind: m[4],
}))

const provRows = provinces.map(
  (m) => `  ('${m[1]}', '${esc(m[2])}', '${esc(String(m[2]).toLowerCase())}')`
)

const cityRows = cities.map(
  (c) =>
    `  ('${c.id}', '${c.province_id}', '${esc(c.name)}', '${c.kind}', '${esc(c.name.toLowerCase())}')`
)

const out = `-- Generated by scripts/build-d1-ref-wilayah.mjs — jangan edit manual.
-- Seed ref_provinces & ref_cities untuk D1 (SQLite).

INSERT OR REPLACE INTO ref_provinces (id, name, name_lower) VALUES
${provRows.join(',\n')};

INSERT OR REPLACE INTO ref_cities (id, province_id, name, kind, name_lower) VALUES
${cityRows.join(',\n')};
`

fs.writeFileSync(outPath, out, 'utf8')
console.log('Wrote', outPath, { provinces: provRows.length, cities: cityRows.length })
